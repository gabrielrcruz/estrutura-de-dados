public class AulasJAVA {

    public static void main(String[] args) {
        int A = 5;
        float salario = 1500.33f;
        int J = 3;
        System.out.println(5 + 3 * 2 - 6 / 4);
        System.out.println((5 + 3) * 2 - 6 / 4);
        System.out.println((5 + 3) * (2 - 6 / 4));
        System.out.println((5 + 3 * 2) - 6 / 4);
        System.out.println(((5 + 3 * 2) - 6) / 4);
        System.out.println((float) J / A * 2 - A);
        System.out.println(Math.sqrt (21 + 4) * 2 / 4);
        System.out.println(20 / J / J + Math.pow (A, J));
        System.out.println(Math.pow (Math.sqrt (-1 + A * 2 ), J));
        System.out.println(salario + salario / 100 * (A * J));
    }
}
